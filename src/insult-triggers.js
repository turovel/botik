export const INSULT_TRIGGERS = uniqueTriggers([
  ...GO_PHRASES,
  ...EXPLICIT_STANDALONE_PHRASES,
  ...DIRECT_INSULT_PHRASES,
  ...ADJECTIVE_OBJECT_PHRASES,
]);

const NORMALIZED_INSULT_TRIGGERS = INSULT_TRIGGERS.map(normalizeTrigger).filter(Boolean);

export function containsInsultTrigger(content) {
  const normalizedContent = ` ${normalizeTrigger(content)} `;

  return NORMALIZED_INSULT_TRIGGERS.some((trigger) =>
    normalizedContent.includes(` ${trigger} `),
  );
}

function uniqueTriggers(triggers) {
  const seen = new Set();

  return triggers.filter((trigger) => {
    const normalized = normalizeTrigger(trigger);

    if (!normalized || seen.has(normalized)) {
      return false;
    }

    seen.add(normalized);
    return true;
  });
}

function normalizeTrigger(content) {
  return String(content ?? '')
    .toLowerCase()
    .replaceAll('ё', 'е')
    .replace(/[^\p{L}\p{N}_]+/giu, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}
